function createIndexSource(): string {
  return `export * from './catalog'
export * from './domain.meta'
export * from './identifiers'
export * from './types'
`
}

function createGeneratedTypesSource(domain: Record<string, unknown>): string {
  const baseTypeImports = GENERATED_IDENTITY_SPECS
    .map(spec => spec.baseTypeName)
    .join(', ')

  const blocks = GENERATED_IDENTITY_SPECS.map((spec) => {
    const entries = createUniqueKeys(toEntityMeta(domain[spec.collectionKey]))
    const entryLiteral = JSON.stringify(entries, null, 2)
    const allName = `all${spec.generatedName}s`

    return `  export const ${spec.generatedName} = ${entryLiteral} as const
  export type ${spec.generatedName}
    = keyof typeof ${spec.generatedName} extends never
      ? ${spec.baseTypeName}
      : typeof ${spec.generatedName}[keyof typeof ${spec.generatedName}]
  export const ${allName} = Object.values(${spec.generatedName}) as ${spec.generatedName}[]
`
  }).join('\n')

  return `// Auto-generated by @endge/codegen. Do not edit manually.
import type { ${baseTypeImports} } from '@endge/core'

export namespace EndgeGen {
${blocks}}
`
}

export interface GenerateDomainArtifactsOptions {
  /** Папка, в которую писать сгенерированные файлы. По умолчанию: process.cwd()/src/gen */
  outputDir?: string
}

const DEFAULT_OUTPUT_DIR = path.join(process.cwd(), 'src', 'gen')

export async function generateDomainArtifacts(
  bundle: EndgeDomainBundle,
  options: GenerateDomainArtifactsOptions = {},
): Promise<GenerateResult> {
  const outputDir = path.resolve(options.outputDir ?? DEFAULT_OUTPUT_DIR)
  const domain = ensureObject(bundle.domain)

  await mkdir(outputDir, { recursive: true })

  const files = [
    { name: 'domain.json', contents: stringifyJson(bundle) },
    { name: 'domain.meta.ts', contents: createMetaSource(bundle) },
    { name: 'catalog.ts', contents: createCatalogSource() },
    { name: 'identifiers.ts', contents: createIdentifiersSource(domain) },
    { name: 'types.ts', contents: createGeneratedTypesSource(domain) },
    { name: 'index.ts', contents: createIndexSource() },
  ]

  await Promise.all(files.map(file =>
    writeFile(path.join(outputDir, file.name), file.contents, 'utf8'),
  ))

  return {
    outputDir,
    files: files.map(file => path.join(outputDir, file.name)),
  }
}
