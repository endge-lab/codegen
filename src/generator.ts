function stringifyJson(value: unknown): string {
  return `${JSON.stringify(value, null, 2)}\n`
}

function ensureObject(value: unknown): Record<string, unknown> {
  return value && typeof value === 'object' && !Array.isArray(value)
    ? value as Record<string, unknown>
    : {}
}

function ensureArray(value: unknown): Record<string, unknown>[] {
  return Array.isArray(value)
    ? value.filter(item => item && typeof item === 'object') as Record<string, unknown>[]
    : []
}

function toText(value: unknown, fallback: string): string {
  const text = typeof value === 'string' ? value.trim() : String(value ?? '').trim()
  return text || fallback
}

function toEntityMeta(items: unknown): EntityMeta[] {
  return ensureArray(items).map((item, index) => {
    const identity = toText(item.identity, `item_${index + 1}`)
    const idValue = item.id
    return {
      id: idValue == null ? identity : String(idValue),
      identity,
      name: toText(item.name ?? item.displayName, identity),
    }
  })
}

function createUniqueKeys(items: EntityMeta[]): Record<string, string> {
  const used = new Set<string>()
  const entries: Array<[string, string]> = []

  for (const item of items) {
    const base = item.identity
      .replace(/[^a-zA-Z0-9]+/g, '_')
      .replace(/^_+|_+$/g, '')
      .replace(/^\d/, '_$&')
      || 'item'

    let candidate = base
    let index = 2
    while (used.has(candidate)) {
      candidate = `${base}_${index}`
      index += 1
    }

    used.add(candidate)
    entries.push([candidate, item.identity])
  }

  return Object.fromEntries(entries)
}

function createIdentifiersSource(domain: Record<string, unknown>): string {
  const pages = createUniqueKeys(toEntityMeta(domain.pages))
  const views = createUniqueKeys(toEntityMeta(domain.views))
  const components = createUniqueKeys(toEntityMeta(domain.components))
  const queries = createUniqueKeys(toEntityMeta(domain.queries))
  const types = createUniqueKeys(toEntityMeta(domain.types))

  return `// Auto-generated by @endge/codegen. Do not edit manually.
export const generatedPageIds = ${JSON.stringify(pages, null, 2)} as const

export const generatedViewIds = ${JSON.stringify(views, null, 2)} as const

export const generatedComponentIds = ${JSON.stringify(components, null, 2)} as const

export const generatedQueryIds = ${JSON.stringify(queries, null, 2)} as const

export const generatedTypeIds = ${JSON.stringify(types, null, 2)} as const
`
}

